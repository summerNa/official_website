<script type="text/javascript">
    window.onload = function () {
        var type = navigator.appName
        if (type == "Netscape") {
            var lang = navigator.language
        }
        else {
            var lang = navigator.userLanguage
        }
        //取得浏览器语言的前两个字母
        var lang = lang.substr(0, 2)
        // 英语
        if (lang == "en") {
            window.location.href = "en1/home.html"
        }
        // 中文 - 不分繁体和简体
        else if (lang == "zh") {
             window.location.href="zh1/home.html"
            //  注释掉了上面跳转,不然会陷入无限循环
        }
        // 除上面所列的语言
        else {
            window.location.href = "en1/home.html"
        }
    }
</script>

